var char = prompt("Enter a character: ");

if (char == "a" || char =="e" || char == "i" || char == "o" || char == "u" || 
    char == "A" || char == "E" || char == "I" || char == "O" || char == "U") {
    document.write(char + " is a vowel.");
}
else {
    document.write(char + " is a consonant.");
};

